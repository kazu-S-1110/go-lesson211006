package main

import "fmt"

// 比較演算子

func main() {
	fmt.Println(1 == 1) //true
	fmt.Println(1 == 2) //false

	fmt.Println(4 <= 6)   //true
	fmt.Println(74 >= 43) //true

	fmt.Println(1 < 4) // true
	fmt.Println(4 > 5) //false

	fmt.Println(true == false) //false
	fmt.Println(true != false) //true

}
